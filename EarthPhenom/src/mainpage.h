/*! \mainpage EarthPhenom Package: simulating gamma-ray emission from the Earth source for LAT
* 
*
* \author Keith Bechtol  bechtol@stanford.edu 
*
* @section introduction Introduction
*
* EarthPhenom is a package that simulates gamma-ray emission from the Earth.
* The model contain two components:
* - Earth limb region (nadir angles 50-70 deg)
* - Inner Earth region (nadir angles < 50 deg)
*
* There are 3 parameters for each model, supplied in the source xml file:
*
* Scale - Scale factor for overall normalization ('1' corresponds to LAT-observed normalization)
* EMin - Minimum simulated energy (Mev)
* EMax - Maximum simulated energy (MeV)
*/

